const passport = require("passport");

//Local Strategy for comparing passwords
const LocalStrategy = require("passport-local").Strategy;
passport.use(new LocalStrategy(
  function(username, password, done) {
    User.getUserByUsername(username, function(err, user){
      if(err) throw err;
      if(!user){
        return done(null, false, {message: "Incorrect Username/Invalid User"});
      }
      User.comparePassword(password, user.password, function(err, isMatch){
        if(err) throw err;
     	if(isMatch){
     	  return done(null, user);
     	} else {
     	  return done(null, false, {message: "Invalid/Incorrect Password"});
     	}
     });
   });
  }
));

//defining to passport package how to serialize users to the app
passport.serializeUser(function(user, done) {
  done(null, user.id);
});

passport.deserializeUser(function(id, done) {
  User.getUserById(id, function(err, user) {
    done(err, user);
  });
});

module.exports = passport;